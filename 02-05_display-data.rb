# first method -- using the puts() function | write/put to the standard output (PUT to S)
puts("Hello, Dragan!")  # puts() adds new line to the data being displayed
# e.g.

puts()  # empty line

puts("Hello, World!", "Goodbye, World!")

puts("now", "is", "the", "time", "for", "all", "good", "people")

# second method -- using the print() function
print("Hello, World!")  # there is NO new line added after data!
print("Hello, World!", " Goodbye, World!", "\n")

# third method -- using the formatted print | printf() function
printf("Hello, Драган!\n")
